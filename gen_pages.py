all_titles = [(p["file"], p["title"], p["icon"]) for p in pages]

for page in pages:
    # topic cards
    cards = ""
    for i, (title, desc) in enumerate(page["topics"]):
        num = str(i+1).zfill(2)
        cards += f'''    <a href="#" class="topic-card">
      <span class="topic-num">— {num}</span>
      <h3>{title}</h3>
      <p>{desc}</p>
      <span class="topic-arrow">→</span>
    </a>\n'''

    # other areas links
    others = ""
    for f, t, ic in all_titles:
        if f != page["file"]:
            others += f'    <a href="{f}" class="area-pill">{ic} {t}</a>\n'

    # title formatting
    words = page["title"].split()
    if len(words) >= 2:
        title_split = " ".join(words[:-1]) + f' <em>{words[-1]}</em>'
    else:
        title_split = f'<em>{page["title"]}</em>'

    html = TEMPLATE.format(
        title=page["title"],
        title_upper=page["title"].upper(),
        title_split=title_split,
        icon=page["icon"],
        desc=page["desc"],
        topic_cards=cards,
        other_links=others,
    )

    with open(f'/home/claude/taymaz-hukuk/{page["file"]}', 'w', encoding='utf-8') as f:
        f.write(html)
    print(f"Created: {page['file']}")

print("All done!")
